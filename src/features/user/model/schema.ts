import { Schema } from "mongoose";
import IUser from "./type";

const UserSchema = new Schema<IUser>({
  first_name: {
    type: String,
    required: true,
  },

  last_name: {
    type: String,
    required: true,
  },

  email: {
    type: String,
    required: true,
    unique: true,
  },

  password: {
    type: String,
    required: true,
  },
});

export default UserSchema;
